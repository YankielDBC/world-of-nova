export function normalizeRace(value) {
    const race = String(value || '').trim().toLowerCase();
    if (race === 'zolk')
        return 'zolk';
    if (race === 'uren')
        return 'uren';
    return null;
}
export function getLocalizedText3(text, lang) {
    if (lang === 'en')
        return text.en;
    if (lang === 'ru')
        return text.ru;
    return text.es;
}
export function getAllRacialTalents() {
    return TALENTS.slice();
}
export function getRacialTalentsForRace(race) {
    return TALENTS.filter((talent) => talent.race === race).sort((a, b) => a.sortOrder - b.sortOrder);
}
export function getRacialTalentByKey(key) {
    const normalized = String(key || '').trim().toLowerCase();
    return TALENTS.find((talent) => talent.key === normalized) || null;
}
export function getRacialPointsForLevel(level) {
    const safeLevel = Math.max(1, Math.floor(level || 1));
    if (safeLevel < 3) {
        return 0;
    }
    return Math.floor((safeLevel - 1) / 2);
}
